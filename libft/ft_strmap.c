#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char		*tmp;
	size_t		i;

	i = 0;
	if (!s || !f)
		return (NULL);
	if (!(tmp = (char *)malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (NULL);
	while (s[i])
	{
		tmp[i] = f(s[i]);
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
