#include "libft.h"

void	ft_putnbr(long long int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n *= -1;
			ft_putchar('-');
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putchar((n % 10) + '0');
		}
		if (n < 10)
			ft_putchar(n + '0');
	}
}
