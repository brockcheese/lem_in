#include "libft.h"

char	*ft_strccpy(char *dst, const char *src, char d)
{
	size_t i;

	i = 0;
	while (src[i] != '\0' && src[i] != d)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
